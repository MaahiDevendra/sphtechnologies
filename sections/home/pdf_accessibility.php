          <p data-aos="fade-up">
            A document or application is considered accessible if meets certain
            technical criteria and can be used by people with disabilities. This
            includes access by people who are mobility impaired, blind, low
            vision, deaf, hard of hearing, or who have cognitive impairments.
            Accessibility features in Adobe Acrobat, Adobe Reader and in the
            Portable Document Format (PDF) make it easier for people with
            disabilities to use PDF documents and forms, with and without the
            aid of assistive technology software and devices such as screen
            readers, screen magnifiers, text-to-speech software, speech
            recognition software, alternative input devices, Braille embossers,
            and refreshable Braille displays.
          </p>
          <p data-aos="fade-up">
            The Web Content Accessibility Guidelines (WCAG) 2.0 and the PDF/UA
            (ISO 14289-1) standard cover a wide range of recommendations for
            making content more accessible to people with disabilities. One
            benefit of following these guidelines is that content becomes more
            usable for all users. For example, the underlying document structure
            that makes it possible for a screen reader to properly read a PDF
            out loud also makes it possible for a mobile device to correctly
            reflow and display the document on a small screen. Similarly, the
            preset tab order of an accessible PDF form helps all users—not just
            users who rely on the keyboard—complete the form more easily
          </p>
